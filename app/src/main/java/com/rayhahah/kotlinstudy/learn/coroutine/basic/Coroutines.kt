fun startAsync(block: suspend () -> Unit) {
    block.startCoroutine(BasicContinuation())
}

suspend fun getUrlByNet(url: String) = suspendCoroutine<String>({
    //这个参数就相当于协程回调函数
    continuation ->
    AsyncTask {
        Thread.sleep(1000)
        if (url.isNotEmpty()) {
            continuation.resume(url + "response")
        } else {
            continuation.resumeWithException(UnsupportedOperationException())
        }
    }
})

fun testCoroutine() {
    l("start")
    startAsync {
        //这里是主线程
        l("startAsync")
        var urlByNet = getUrlByNet("hhh")
        //这里是协程的线程，所以协程内部需要对线程进行切换
        l("stopAsync")
    }
}
